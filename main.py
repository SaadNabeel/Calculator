def add(a,b):
    return a+b
def sub(a,b):
    return a-b
def mult(a,b):
    return a*b
def divi(a,b):
    return a/b
x=int(input("Enter the first number:"))
y=int(input("Enter the second number:"))
print("Addition is: ", add(x,y))
print("Subtraction is: ", sub(x,y))
print("Multiplication is: ", mult(x,y))
print("Division is: ", divi(x,y))